package service

// pptOutlineSpecification 定义了 PPT 大纲生成的标准化规范
// 适配学习笔记、知识讲解、培训课件等学习场景
const pptOutlineSpecification = `
# PPT 大纲生成规范

## 1. 核心原则

### 1.1 逻辑闭环原则
整体遵循"引入-阐述-论证-总结-落地"完整逻辑链，各章节、各页面内容层层递进，无逻辑断层、无内容脱节。

### 1.2 聚焦核心原则
所有内容围绕 PPT 核心主题展开，剔除无关、冗余、重复内容，每一页均有明确的核心输出点。

### 1.3 层级清晰原则
严格区分一级大纲、二级大纲、页面内容，层级划分统一、规整，从属关系明确。

### 1.4 落地适配原则
大纲内容适配可视化呈现，避免大段文字堆砌，预留图表、数据、案例、配图的填充空间。

### 1.5 场景适配原则
根据学习内容和受众群体调整内容详略与结构侧重，贴合学习需求。

## 2. 大纲整体结构标准

### 2.1 标准整体框架（通用必选）

**开篇部分**：封面、目录、前言/背景
- 封面页：主题、副标题、作者/讲师、日期
- 目录页：罗列全部一级核心章节
- 前言/背景页：铺垫场景、说明初衷、引出主题

**主体部分**：核心内容阐述、现状/问题分析、核心价值/亮点展示
- 核心定义/概述页：核心概念定义、核心内涵、覆盖范围
- 现状/成果展示页：现有基础、阶段性成果、核心数据
- 问题/痛点分析页：现存问题、产生原因、影响范围

**落地部分**：实施方案、推进计划、保障措施、数据成果
- 核心方案/对策页：解决方案、核心思路、整体策略
- 实施计划/进度页：分阶段目标、时间节点、核心任务
- 资源/保障支撑页：人力、物力、技术、制度等支撑条件

**总结部分**：核心总结、未来规划、风险与优化
- 核心总结页：提炼全文核心内容，汇总关键举措
- 未来规划/优化方向页：后续目标、长期规划、迭代方向

**收尾部分**：致谢、答疑
- 致谢页：感谢聆听、敬请指导
- 答疑页：疑问答疑、交流探讨

### 2.2 场景化可选模块
根据 PPT 用途灵活新增：案例展示、对比分析、知识点解析、实操步骤、总结回顾。

## 3. 分级编写规范

### 3.1 一级大纲（章节级）
为 PPT 核心大章节，统领一个完整内容板块，字数简洁凝练（四字/六字/短语式标题）。
编号：一、二、三、……
示例：一、概念框架、二、原理机制、三、过程步骤、四、应用场景

### 3.2 二级大纲（页面组级）
为一级章节下的细分板块，拆分章节核心维度，每个二级大纲可对应 1 个或多个同类页面。
编号：1.、2.、3.、……
示例：1. 核心定义、2. 关键特征、3. 与其他概念的关系

### 3.3 三级大纲（页面内容级）
为单页 PPT 的具体内容要点，简洁具体、直击重点，明确页面需要呈现的文字、数据、图表、案例等内容。
编号：（1）、（2）、（3）、……
示例：（1）概念的核心定义；（2）概念的三个关键特征；（3）概念在实际中的应用场景

## 4. 各页面大纲详细规范

### 4.1 开篇导入模块

#### 4.1.1 封面页
核心要求：信息完整、主题醒目。
必备内容：PPT 主标题、副标题（可选）、作者/讲师、日期。

#### 4.1.2 目录页
核心要求：罗列全部一级核心章节，简洁规整，不展示三级细节。

#### 4.1.3 前言/背景页
核心作用：铺垫场景、说明初衷、引出主题。
必备要点：学习背景、知识现状、学习痛点、本次学习的核心目的。

### 4.2 核心主体模块

#### 4.2.1 核心定义/概述页
适用于概念、原理、新方法类 PPT。
核心要点：核心名词定义、核心内涵、覆盖范围、核心价值。

#### 4.2.2 现状/成果展示页
适用于学习总结、知识复盘类 PPT。
核心要点：现有基础、阶段性成果、核心数据、已掌握内容。

#### 4.2.3 问题/痛点分析页
核心要点：精准罗列现存问题、问题产生原因、影响范围、核心痛点。

### 4.3 落地执行模块

#### 4.3.1 核心方案/对策页
针对前文问题、需求，输出对应解决方案、核心思路、整体策略。

#### 4.3.2 实施计划/进度页
核心要点：分阶段推进目标、时间节点、核心工作内容、进度安排。

#### 4.3.3 资源/保障支撑页
核心要点：学习资源、工具支持、制度保障等支撑条件。

### 4.4 总结规划模块


#### 4.4.1 核心总结页
提炼全文核心内容，汇总核心成果、关键举措、解决的核心问题。

#### 4.4.2 未来规划/优化方向页
核心要点：后续学习目标、长期规划、迭代优化方向、重点推进事项。

### 4.5 收尾模块

#### 4.5.1 致谢页
固定简洁内容：感谢聆听、敬请指导。

#### 4.5.2 答疑页
固定内容：疑问答疑、交流探讨。

## 5. 内容撰写规范

### 5.1 文字要求
- **简洁精炼**：摒弃空话、套话、冗余修饰，每一条大纲要点直击核心
- **精准具体**：拒绝模糊表述，优先量化、具象化
- **统一话术**：全文句式、表述风格统一，无口语化用语
- **逻辑对仗**：同级标题句式、结构尽量统一

### 5.2 页面容量要求
- 单页 PPT 大纲核心要点控制在 3-6 条
- 要点过多需拆分页面，避免单页内容过载
- 单一页面只聚焦一个核心主题

### 5.3 可视化适配要求
大纲生成时需同步标注适配形式：
- 数据类内容：标注「适配图表（柱状图/折线图/饼图）」
- 流程类内容：标注「适配时间轴/流程图」
- 对比类内容：标注「适配对比表格」
- 要点类内容：标注「适配分段图文」

## 6. 格式排版规范

### 6.1 层级格式统一
- 一级标题：中文大写数字（一、二、三），加粗，独立成段
- 二级标题：阿拉伯数字（1.2.3.），常规字体，独立成段
- 三级要点：括号数字（（1）（2）（3）），逐条罗列

### 6.2 整体排版要求
- 段落间距统一，层级缩进规整
- 无错乱、无重复编号、无跳号
- 全文标题命名风格统一，长短均衡

## 7. 场景化适配调整规则

### 7.1 学习笔记类 PPT
侧重：概念讲解、知识点拆分、案例解析、重难点梳理、实操步骤、总结回顾，内容细致、条理清晰、通俗易懂。

### 7.2 培训课件类 PPT
侧重：概念讲解、原理机制、流程步骤、应用场景、易错点、总结复盘，内容系统、逻辑严谨。

### 7.3 知识分享类 PPT
侧重：核心亮点、价值优势、核心成果、差异化亮点、落地前景，精简细节，突出重点。

## 8. 审核验收标准

生成的 PPT 大纲需满足以下标准：
- **结构完整**：核心五大模块齐全，无关键内容缺失，逻辑闭环
- **层级清晰**：三级层级划分规范，从属关系明确，无层级混乱
- **内容聚焦**：无冗余、无偏离主题，每一页核心重点明确
- **落地可行**：可直接根据大纲制作 PPT，无需二次梳理逻辑
- **格式规范**：排版统一、话术标准、无文字错误、适配场景
`
