package service

// pptOutlineSpecification 定义了 PPT 大纲生成的标准化规范
// 适配学习笔记、知识讲解、培训课件等学习场景
const pptOutlineSpecification = `
# PPT 大纲生成规范（学习场景）

## 1. 核心原则

### 1.1 知识闭环原则
整体遵循"定义→原理→方法→应用→辨析→整合"完整知识链，各章节、各页面内容层层递进，无逻辑断层、无知识脱节。

### 1.2 具体性原则
所有要点必须是具体的知识颗粒，而非笼统的主题描述。
差：'光反应的过程'  好：'类囊体膜色素系统吸收光能驱动电子传递'
差：'Go的基本语法'  好：'Go使用var声明变量，支持类型推断'

### 1.3 去文字化原则
提炼核心论点、关键数据、因果逻辑，避免照搬原文段落。每个要点应自成一体，包含定义→原理→证据→应用的结构。

### 1.4 信息密度原则
每个内容页必须包含足够多的实质内容，避免只有标题和两行字的空泛页面。单页至少 4-6 个具体知识要点。

### 1.5 学习导向原则
面向学习者而非汇报者，注重"理解"而非"知晓"。标注易错点、边界条件、常见误区，用具体例子和量化数据让抽象概念可感知。

## 2. 大纲整体结构标准（学习六大模块）

### 2.1 开篇导入模块
- **封面页**：主题、副标题（一句话概括学习目标）
- **目录页**：罗列全部核心章节
- **学习背景页**：为什么要学这个主题？解决什么问题？前置知识有哪些？

### 2.2 概念理解模块
- **核心定义页**：主题的核心概念定义、关键特征、适用范围
- **概念关系页**：与相关概念的区别和联系、概念图谱

### 2.3 原理机制模块
- **运作原理页**：核心原理、因果逻辑、内部结构
- **关键机制页**：关键组件/步骤如何协同工作、数据流向、因果关系

### 2.4 过程方法模块
- **关键步骤页**：操作流程、决策树、分阶段目标
- **方法对比页**：不同方法的优劣对比、适用场景

### 2.5 应用拓展模块
- **应用场景页**：真实案例、典型应用、最佳实践
- **易错辨析页**：常见误区、边界条件、错误示例与纠正

### 2.6 收束整合模块
- **知识图谱页**：核心知识点之间的关系图、关键结论
- **延伸学习页**：下一步学习建议、推荐资源、开放问题

## 3. 分级编写规范

### 3.1 一级大纲（章节级）
为 PPT 核心大章节，统领一个完整知识板块，字数简洁凝练（四字/六字/短语式标题）。
示例：概念框架、原理机制、过程步骤、应用场景

### 3.2 二级大纲（页面组级）
为一级章节下的细分板块，每个二级大纲对应 1-3 个同类页面。
示例：核心定义、关键特征、与其他概念的关系

### 3.3 三级大纲（页面内容级）
为单页 PPT 的具体内容要点，必须具体、可展开、有实质信息。
示例：（1）Go语言采用静态类型系统，编译期发现类型错误；（2）内置垃圾回收器采用三色标记算法，暂停时间通常在毫秒级

## 4. 要点编写规范

### 4.1 具体性要求
- 差：'基本原理'  好：'TCP通过三次握手建立可靠连接'
- 差：'核心概念'  好：'Docker镜像是只读模板，容器是镜像的运行实例'
- 差：'应用场景'  好：'Redis缓存适用于读多写少、QPS超过1万的场景'

### 4.2 数据驱动
- 材料中的关键数据、公式、定理必须提取到要点中
- 优先保留量化信息：'暂停时间通常在1-5ms'优于'暂停时间很短'

### 4.3 代码块保留
- 当材料中包含代码块时，必须原样保留代码，使用三个反引号围栏标记包裹

## 5. 各页面大纲详细规范

### 5.1 开篇导入模块

#### 5.1.1 封面页
必备内容：PPT 主标题、副标题（一句话学习目标）、标签关键词。

#### 5.1.2 目录页
罗列全部核心章节，简洁规整。

#### 5.1.3 学习背景页
必备要点：学习背景（为什么要学）、核心问题（解决什么）、前置知识（需要哪些基础）。

### 5.2 概念理解模块

#### 5.2.1 核心定义页
必备要点：核心名词定义、关键特征（至少3个）、适用范围和限制。

#### 5.2.2 概念关系页
必备要点：与相关概念的区别、概念之间的关系图、典型分类。

### 5.3 原理机制模块

#### 5.3.1 运作原理页
必备要点：核心原理描述、因果逻辑链、内部组件/步骤如何协同。

#### 5.3.2 关键机制页
必备要点：关键组件/步骤详解、数据流向、因果关系。

### 5.4 过程方法模块

#### 5.4.1 关键步骤页
必备要点：操作流程（有序步骤）、决策节点、分阶段目标。

#### 5.4.2 方法对比页
必备要点：不同方法的优劣、适用场景、选择建议。

### 5.5 应用拓展模块

#### 5.5.1 应用场景页
必备要点：真实案例（具体场景+数据）、典型应用、最佳实践。

#### 5.5.2 易错辨析页
必备要点：常见误区（至少2-3个）、边界条件、错误示例与纠正。

### 5.6 收束整合模块

#### 5.6.1 知识图谱页
必备要点：核心知识点之间的关系、关键结论总结、知识结构回顾。

#### 5.6.2 延伸学习页
必备要点：下一步学习建议、推荐资源、开放问题。

## 6. 格式排版规范

### 6.1 层级格式统一
- 一级标题：中文大写数字（一、二、三），加粗，独立成段
- 二级标题：阿拉伯数字（1.2.3.），常规字体，独立成段
- 三级要点：括号数字（（1）（2）（3）），逐条罗列

### 6.2 整体排版要求
- 段落间距统一，层级缩进规整
- 无错乱、无重复编号、无跳号
- 全文标题命名风格统一，长短均衡

## 7. 场景化适配调整规则

### 7.1 学习笔记类 PPT
侧重：概念讲解、知识点拆分、案例解析、重难点梳理、实操步骤、总结回顾，内容细致、条理清晰、通俗易懂。
材料短时需补充知识背景：原理推导、历史背景、典型例子、对比类比。

### 7.2 培训课件类 PPT
侧重：概念讲解、原理机制、流程步骤、应用场景、易错点、总结复盘，内容系统、逻辑严谨。
材料短时需补充实操演练步骤和常见问题排查。

### 7.3 知识分享类 PPT
侧重：核心亮点、价值优势、核心成果、差异化亮点、落地前景，精简细节，突出重点。
材料短时需补充行业背景和竞品对比。

## 8. 审核验收标准

生成的大纲需满足以下标准：
- **结构完整**：六大核心模块齐全，无关键内容缺失，知识闭环
- **具体充实**：每个要点是具体的知识颗粒，不是笼统主题词
- **数据保留**：材料中的关键数据、公式、定理未被遗漏
- **层级清晰**：三级层级划分规范，从属关系明确，无层级混乱
- **内容聚焦**：无冗余、无偏离主题，每一页核心重点明确
- **学习导向**：有定义、有原理、有应用、有辨析
- **格式规范**：排版统一、话术标准、无文字错误、适配场景
`
